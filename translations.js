function setLanguage(lang) {
    // Save preference
    localStorage.setItem('sb_lang', lang);

    // Update content
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        // Check if translation exists for this language, otherwise fallback to Turkish (or English)
        if (translations[lang] && translations[lang][key]) {
            el.innerText = translations[lang][key];
        } else if (translations['tr'][key]) {
            el.innerText = translations['tr'][key]; // Fallback
        }
    });

    // Update HTML lang attribute
    document.documentElement.lang = lang;
}

// Init
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('sb_lang') || 'tr';
    const selector = document.getElementById('language-selector');
    if (selector) {
        selector.value = savedLang;
        selector.addEventListener('change', (e) => {
            setLanguage(e.target.value);
        });
    }
    setLanguage(savedLang);
});
